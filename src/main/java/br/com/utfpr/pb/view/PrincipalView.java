    private void addMenus() {
        for (Menu menu : Menu.values()) {
            Icon icon = new ImageIcon(getClass().getResource("/icon/" + menu.getIcon()));

            AbstractAction action = new AbstractAction(menu.getDescricao(), icon) {
                @Override
                public void actionPerformed(ActionEvent e) {
                    try {
                        Class.forName("br.com.utfpr.pb.view." + menu.getClazz()).newInstance();
                    } catch (Exception ex) {
                        ex.printStackTrace();
                    }
                }
            };

            if (menu.getTask().equals("cadastros")) {
                taskPaneCadastros.add(action);
            } else if (menu.getTask().equals("pedidos")) {
                taskPanePedidos.add(action);
            } else if (menu.getTask().equals("financeiro")) {
                taskPaneFinanceiro.add(action);
            } else if (menu.getTask().equals("relatorios")) {
                jMenuRelatorios.add(new JMenuItem(action));
            } else {
                jMenuGraficos.add(new JMenuItem(action));
            }
        }
    }

