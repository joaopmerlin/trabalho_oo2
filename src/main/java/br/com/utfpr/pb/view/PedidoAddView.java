    private void salvar() {
        pedidoItem.setValorUnitario(doubleUtil.getDouble(valor.getText()));
        pedidoItem.setQuantidade(Integer.parseInt(quantidade.getText()));

        if (pedidoItem.getPedido().getTipoPedido().equals(TipoPedido.VENDA) &&
                pedidoItem.getQuantidade() > pedidoItem.getProduto().getEstoque()) {
            if (JOptionPane.showConfirmDialog(null, "Quantidade informada Ã© maior que o estoque! Deseja continuar?") == 0) {
                dispose();
            }
        } else {
            dispose();
        }
    }
    
    private void calculaTotal(){
        try {
            Double t = doubleUtil.getDouble(valor.getText()) * Integer.parseInt(quantidade.getText());
            total.setText(doubleUtil.format(t));
        } catch (Exception e) {
            total.setText("0,00");
        }
    }

    public void setPedidoItem(PedidoItem pedidoItem) {
        this.pedidoItem = pedidoItem;
        produto.setText(pedidoItem.getProduto().getId() + " - " + pedidoItem.getProduto().getDescricao());
        valor.setText(doubleUtil.format(pedidoItem.getValorUnitario()));
        quantidade.setText(pedidoItem.getQuantidade().toString());
        total.setText(doubleUtil.format(pedidoItem.getValorTotal()));
    }

    public PedidoItem getPedidoItem() {
        return pedidoItem;
    }

